import java.util.*;

class Array{
	
	public static void main(String args[]){
		Scanner read = new Scanner(System.in);
		float n=read.nextFloat();
		float a[]=new float[n];
		for(int i=0;i<n;i++){
			
			a[i]=read.nextLong();
		} 
		for(int i=0;i<n;i++){
			
			System.out.print(a[i]+" ");
		} 
		
		
		
		
		
	}
	
}